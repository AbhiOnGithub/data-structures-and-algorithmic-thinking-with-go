package main

import (
	"fmt"
)

func Reverse(s string) string {
	r := []rune(s)
	for i, j := 0, len(r)-1; i < len(r)/2; i, j = i+1, j-1 {
		r[i], r[j] = r[j], r[i]
	}
	return string(r)
}

func Reverse2(s string) string {
	r := []rune(s)
	for i, j := 0, len(r)-1; i < len(r)/2; i, j = i+1, j-1 {
		r[i] ^= r[j]
		r[j] ^= r[i]
		r[i] ^= r[j]
	}
	return string(r)
}

func main() {
	fmt.Println(Reverse("Hello, world"))
	fmt.Println(Reverse2("Hello, world"))
}
